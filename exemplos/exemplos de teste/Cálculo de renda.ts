//Nesse exemplo, declaramos uma constante numero com o valor 7. Em seguida, usamos uma instrução if 
//para verificar se o número é par ou ímpar. Se o número for divisível por 2 e o resto da divisão for 
//igual a zero, a mensagem "Par" é impressa no console.Caso contrário, a mensagem "Ímpar" é impressa.

const numero: number = 7;

if (numero % 2 === 0) {
  console.log("Par");
} else {
  console.log("Ímpar");
}